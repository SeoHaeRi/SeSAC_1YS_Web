package sesac.spring.api.sesacapi.vo;

public class InfoVO {
    private String name;
    private String gender;
    private String birth;
    private String favorite;

    public String getName() {
        return name;
    }

    public String getGender() {
        return gender;
    }

    public String getBirth() {
        return birth;
    }

    public String getFavorite() {
        return favorite;
    }
}
